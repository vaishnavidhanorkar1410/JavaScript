// Wap program to print numbers from 5 to 15 by incrementing 1
console.log("To print numbers from 5 to 15 by incrementing 1");

let i=5;  // initialization
while (i<=15) {  // condition
   console.log(i);
   i++; // update expression
}

//WAP to print numbers from 50 to 40 by decrementing by 1
console.log("To print numbers from 50 to 40 by decrementing 1");

let j=50;  // initialization
while (j>=40) {  // condition
   console.log(j);
   j--; // update expression
}

